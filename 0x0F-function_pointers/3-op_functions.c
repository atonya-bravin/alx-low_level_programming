#include "3-calc.h"

/**
 * op_add - returns sum of two numbers
 *
 * @a: first number
 * @b: second number
 *
 * Return: sum (success)
 */

int op_add(int a, int b)
{
	int first_number = a;
	int second_number = b;

	return (first_number + second_number);
}

/**
 * op_sub - returns difference of two numbers
 *
 * @a: first number
 * @b: second number
 *
 * Return: difference (success)
 */
int op_sub(int a, int b)
{
	int first_number = a;
	int second_number = b;

	return (first_number - second_number);
}

/**
 * op_mul - returns product of two numbers
 *
 * @a: first number
 * @b: second number
 *
 * Return: product (success)
 */
int op_mul(int a, int b)
{
	int first_number = a;
	int second_number = b;

	return (first_number * second_number);
}

/**
 * op_div - returns quotient of two numbers
 *
 * @a: first number
 * @b: second number
 *
 * Return: quotient (success)
 */
int op_div(int a, int b)
{
	int first_number = a;
	int second_number = b;

	return (first_number / second_number);
}

/**
 * op_mod - returns remainder after division of two numbers
 *
 * @a: first number
 * @b: second number
 *
 * Return: remainder (success)
 */
int op_mod(int a, int b)
{
	int first_number = a;
	int second_number = b;

	return (first_number % second_number);
}
