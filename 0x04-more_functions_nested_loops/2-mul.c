/**
 * mul - multiplication function
 *
 * Description: multiplies two numbers and returns the result
 *
 * @a: first number
 * @b: second number
 *
 * Return: Always result
 */
int mul(int a, int b)
{
	int first_number = a;
	int second_number = b;

	return (first_number * second_number);
}
